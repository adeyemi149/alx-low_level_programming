#include <stdio.h>
#include "main.h"

/*
 *main - prints the number from 1 - 100 followed by new line
 *nut multiple of 3 prints Fizz, multiple of 5 prints Buzz, 
 *multiple of 3 and 5 prints FizzBuzz
 *Returns : 0
 */

int main(void)
{
	int i;

	for (i = 1; i <= 100; i++)
	{
		if (i % 3 == 0 && i % 5 != 0)
		{
			printf(" Fizz");
		} else if (i % 5 == 0 && i % 3 != 0)
		{
			printf(" Buzz");
		} else if (i % 3 == 0 && i % 5 == 0)
		{
			printf(" FizzBuzz");
		} else if (i == 1)
		{
			printf("%d", i);
		} else
		{
			printf(" %d", i);
		}
	}
	printf("\n");

	return (0);
}
